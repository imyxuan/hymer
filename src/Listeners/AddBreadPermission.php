<?php

namespace PickOne\Hymer\Listeners;

use PickOne\Hymer\Events\BreadAdded;
use PickOne\Hymer\Facades\Hymer;

class AddBreadPermission
{
    /**
     * Create the event listener.
     *
     * @return void
     */
    public function __construct()
    {
        //
    }

    /**
     * Create Permission for a given BREAD.
     *
     * @param BreadAdded $event
     *
     * @return void
     */
    public function handle(BreadAdded $bread)
    {
        if (config('hymer.bread.add_permission') && file_exists(base_path('routes/web.php'))) {
            // Create permission
            //
            // Permission::generateFor(Str::snake($bread->dataType->slug));
            $role = Hymer::model('Role')->where('name', config('hymer.bread.default_role'))->firstOrFail();

            // Get permission for added table
            $permissions = Hymer::model('Permission')->where(['table_name' => $bread->dataType->name])->get()->pluck('id')->all();

            // Assign permission to admin
            $role->permissions()->attach($permissions);
        }
    }
}
